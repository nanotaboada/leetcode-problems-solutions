public class Solution
{
    public int PivotIndex(int[] nums)
    {
        var rightSum = 0;

        for (var i = 0; i < nums.Length; i++)
        {
            rightSum += nums[i];
        }

        var leftSum = 0;

        for (var i = 0; i < nums.Length; i++)
        {
            rightSum -= nums[i];

            if (leftSum == rightSum)
            {
                return i;
            }

            leftSum += nums[i];
        }

        return -1;
    }
}
